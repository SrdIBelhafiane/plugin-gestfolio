    def set_theme(self):
        root = QgsProject.instance().layerTreeRoot()

        for group in root.children():
            if not isinstance(group, QgsLayerTreeGroup):
                continue

            name = group.name()

            # 🔵 CAS 1 : niveau = topo
            if self.niveau == 'topo':
                if name == "NOUVELLE TOPO":
                    self._set_visible_recursive(group, True)
                elif name == "Masque":
                    pass
                else:
                    group.setItemVisibilityChecked(False)

            # 🟢 CAS 2 : autre niveau
            else:
                if name == "NOUVEAU RECOLEMENT":
                    for child in group.children():
                        if isinstance(child, QgsLayerTreeGroup):
                            if child.name().upper() == self.niveau.upper():
                                self._set_visible_recursive(child, True)
                            elif child.name() == "Masque":
                                pass
                            else:
                                child.setItemVisibilityChecked(False)

                    group.setItemVisibilityChecked(True)  # important pour voir les enfants

                elif name == "Masque":
                    pass

                else:
                    group.setItemVisibilityChecked(False)
    def _set_visible_recursive(self, group, visible):
        group.setItemVisibilityChecked(visible)

        for child in group.children():
            if isinstance(child, QgsLayerTreeGroup):
                self._set_visible_recursive(child, visible)
            else:
                child.setItemVisibilityChecked(visible)
