    def copier_group(self):
        self.GROUP_NAME = "RECOLEMENT EXISTANT"
        self.GROUP_NAME2 = "NOUVEAU RECOLEMENT"
        self.ancienne_schema = 'sw_pr_res'
        self.nouvelle_schema = 'recolement'
        self.project = QgsProject.instance()
        root = self.project.layerTreeRoot()

        source_group = root.findGroup(self.GROUP_NAME)
        target_group = root.findGroup(self.GROUP_NAME2)
        if target_group :
            parent = target_group.parent()
            parent.removeChildNode(target_group)
        if source_group:
            self.clone_group(source_group, root)
        
        self.GROUP_NAME = "TOPO EXISTANTE"
        self.GROUP_NAME2 = "NOUVELLE TOPO"
        self.ancienne_schema = 'sw_pr_topo'
        self.project = QgsProject.instance()
        root = self.project.layerTreeRoot()

        source_group = root.findGroup(self.GROUP_NAME)
        target_group = root.findGroup(self.GROUP_NAME2)
        if target_group :
            parent = target_group.parent()
            parent.removeChildNode(target_group)
        if source_group:
            self.clone_group(source_group, root)
        
    
    def clone_group(self, group, parent):
        OLD_SCHEMA = self.ancienne_schema
        NEW_SCHEMA = self.nouvelle_schema
        if group.name() == self.GROUP_NAME :
            new_group = parent.addGroup(self.GROUP_NAME2)
        else :
            new_group = parent.addGroup(group.name())

        for child in group.children():

            if isinstance(child, QgsLayerTreeGroup):
                self.clone_group(child, new_group)

            else:
                layer = child.layer()
                if not layer:
                    continue

                uri = QgsDataSourceUri(layer.source())

                schema = uri.schema()
                table = uri.table()
                geom_col = uri.geometryColumn()

                if schema != OLD_SCHEMA:
                    continue

                # 👉 adapter la colonne géométrique
                if geom_col == "the_geom":
                    new_geom_col = "geom"
                else:
                    new_geom_col = geom_col

                new_uri = QgsDataSourceUri(uri)
                new_uri.setDataSource(
                    NEW_SCHEMA,
                    table,
                    new_geom_col,  
                    uri.sql(),
                    uri.keyColumn()
                )

                new_layer = QgsVectorLayer(
                    new_uri.uri(False),
                    layer.name(),
                    "postgres"
                )

                if new_layer.isValid():

                    # Copier style + étiquettes
                    new_layer.setRenderer(layer.renderer().clone())

                    if layer.labeling():
                        new_layer.setLabeling(layer.labeling().clone())
                        new_layer.setLabelsEnabled(layer.labelsEnabled())

                    self.project.addMapLayer(new_layer, False)
                    new_group.addLayer(new_layer)

                
        return new_group
