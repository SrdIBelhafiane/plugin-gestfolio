    def on_click_general(self, event):
        if event.ydata is None:
            return

        y_index = int(round(event.ydata))

        if y_index < 0 or y_index >= len(self.classes):
            return

        if self.selected_classe != self.classes[y_index] :
            self.selected_classe = self.classes[y_index]
        else :
            self.selected_classe = None
        self.set_graphe_tables()
        if self.selected_classe :
            self.filtre()
        else :
            self.non_filtre()
        for bar, info in self.general_bars.items():
            if self.selected_classe is None :
                bar.set_alpha(1.0)  # barre sélectionnée
            elif info["table"] != self.selected_classe:
                bar.set_alpha(0.2)  # barre non sélectionnée
            else:
                bar.set_alpha(1.0) 
        self.canvas_general.draw()

        
